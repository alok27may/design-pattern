package com.learning.ds.structural.facade;

public class PopcornPopper {
    public void on() {
        System.out.println("Popcorn Popper is On");
    }

    public void pop() {
        System.out.println("Popcorn Popper is ready to pop");
    }

    public void off() {
        System.out.println("Popcorn Popper is Off");
    }
}
